	&lt;strong&gt;第1条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	総則
&lt;/p&gt;
&lt;p&gt;
	本規約は、Greedy Cat Japan株式会社（以下、「甲」という。）がインターネット上で運営するMr.Menu（以下、「Mr.Menu」という。）を利用することを甲所定の方法により申し込み、甲がこれを承諾した場合の、甲とMr.Menu利用者（以下、「乙」という。）との間の契約関係につき定めるものである（以下、本規約に定める契約を「本契約」という。）。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第2条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	出店の申込
&lt;/p&gt;
&lt;p&gt;
	1.乙は、甲に対し、甲が管理するサーバー（以下、「サーバー」という。）においてアカウントを開設し、Mr.Menuを利用すること（以下、「出店」という。）を希望する場合、甲所定の方法により申込みを行わなければならない。
&lt;/p&gt;
&lt;p&gt;
	2.甲は、前項の申込みを承諾した場合、乙に対し、甲が管理するサーバー内に存在する乙のサービス提供のために必要となる甲所定の提供物並びに乙のレストランに提供する日本語メニューを、乙が本規約および甲乙間で適用される他の規約、ガイドラインその他の合意事項（以下、あわせて「本規約等」という。）に従って使用することを許諾する。
&lt;/p&gt;
&lt;p&gt;
	3.甲は、前項の甲所定の提供物について、甲の判断により自由にその仕様を変更し、バージョンアップをすることができる。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第3条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	届出事項
&lt;/p&gt;
&lt;p&gt;
	1.乙は、第2条の申込に際し、以下の事項をあらかじめ甲に届け出るものとし、以下の事項に変更がある場合には、速やかに甲に届け出るものとする。届出がなかったことによる損害は乙の負担とする。
&lt;/p&gt;
&lt;p&gt;
	&amp;nbsp;(1) 商号(屋号)、代表者名および住所
&lt;/p&gt;
&lt;p&gt;
	&amp;nbsp;(2) 出店についての責任者（以下、「管理責任者」という。）の氏名、電子メールアドレス、電話番号その他甲所定の事項
&lt;/p&gt;
&lt;p&gt;
	(3)その他甲が指定する乙の業務に関する事項
&lt;/p&gt;
&lt;p&gt;
	2.甲が前項により届出のあった乙の住所に書面を郵送した場合には、甲が書面を投函した時に意思表示の効果が生じ、また、事実の通知があったものとみなす。
&lt;/p&gt;
&lt;p&gt;
	3.甲が第1項により届出のあった乙の管理責任者の電子メールアドレス（以下、「届出メールアドレス」という。）に電子メールを送信した場合には、当該電子メールは甲が送信した時に乙に到達したものとみなす。
&lt;/p&gt;
&lt;p&gt;
	4.甲が乙に対し、甲のサーバー内の甲所定の管理画面に連絡事項を掲示した旨を届出メールアドレス宛に電子メールにより通知した場合、乙は、速やかに当該連絡事項の確認をしなければならず、甲が当該電子メールを送信した時に当該連絡事項は乙に到達したものとみなす。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第4条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	権利の譲渡等
&lt;/p&gt;
&lt;p&gt;
	乙は、出店する権利その他本契約に基づく一切の権利を譲渡、転貸、担保差入、その他形態を問わず処分することはできない。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第5条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	管理画面の開設
&lt;/p&gt;
&lt;p&gt;
	甲は、乙に対し、第2条第1項の申込を承諾した場合、サーバー内の甲が指定するURLに乙の管理画面を開設するとともに、管理画面にアクセスするために必要となるIDおよびパスワードを発行する（管理画面の開設日を以下、「アカウント発行日」という。）。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第6条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	管理責任者
&lt;/p&gt;
&lt;p&gt;
	1.乙は、本契約に基づく出店および販売等を行うに際して、以下の義務を負う
&lt;/p&gt;
&lt;p&gt;
	&amp;nbsp;(1)管理責任者として関与する者に対し、Mr.Menuに関するシステムおよびその利用方法を十分理解させること
&lt;/p&gt;
&lt;p&gt;
	&amp;nbsp;(2) 管理責任者に甲からのサポート等の連絡に利用するメールボックスを管理させること
&lt;/p&gt;
&lt;p&gt;
	2. 乙は、管理責任者を変更する際には、3条1項（2）の事項を速やかに甲に対して通知するとともに、パスワードの変更手続をしなければならない。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第7条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	著作権等
&lt;/p&gt;
&lt;p&gt;
	1. 出店にかかる著作物については、甲が制作したものは甲が、乙が制作したものは乙が、それぞれ著作権を有する。
&lt;/p&gt;
&lt;p&gt;
	2.乙は、甲に対し、前項に定める乙の著作物について、甲がMr.Menuのプロモーションのため、Mr.Menu内または提携サイトからのハイパーリンク、Mr.MenuのOEM供給等、甲が妥当と判断する方法により無償で使用することを許諾する。ただし、乙の著作物を使用する場合には、予め、乙に対し、使用する著作物の内容、使用の方法について乙の承諾を得るものとする。その上で甲のサービス内のバナー等の作成に関してのみ、承諾をされたものとして使用する。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第8条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	業務委託
&lt;/p&gt;
&lt;p&gt;
	1.甲および乙は、自らの責任において業務の全部または一部を第三者に委託することができる。
&lt;/p&gt;
&lt;p&gt;
	2.前項の場合、甲および乙は当該第三者に対し、顧客情報の管理を徹底するとともに本規約等を遵守させるものとし、当該第三者によるいかなる行為に対しても責任を負うものとする。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第9条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	契約期間
&lt;/p&gt;
&lt;p&gt;
	本契約の有効期間は、アカウント発行日から1年間とする。ただし、期間満了の1ヶ月前までに甲または乙の一方から書面による解約の意思表示がない限り、1年間延長されるものとし、以後も同様とする。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第10条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	基本出店料
&lt;/p&gt;
&lt;p&gt;
	1.乙は、甲に対し、初期費用として、申込書に定める金額を支払う。
&lt;/p&gt;
&lt;p&gt;
	2.乙は、甲に対し、アカウント発行月から当月末日までに申込書に定める初期費用を支払うものとする。乙は、この支払いに関して、甲に対し、乙が支払い義務を有する金額につき、特段の意思表示することなく、甲が乙へ支払う金額と相殺することを認める。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第11条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	システム利用料
&lt;/p&gt;
&lt;p&gt;
	1.乙は、甲に対し、月額費用として、申込書に定める金額を支払う。
&lt;/p&gt;
&lt;p&gt;
	2.乙は、甲に対し、アカウント発行月から当月末日までに申込書に定める月額費用を支払うものとする。乙は、この支払いに関して、甲に対し、乙が支払い義務を有する金額につき、特段の意思表示することなく、甲が乙へ支払う金額と相殺することを認める。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第12条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	出店料等の支払い
&lt;/p&gt;
&lt;p&gt;
	1.基本出店料、システム利用料、その他本契約に関して乙から甲に支払われる金銭（以下、「出店料等」という。）の支払い方法については、甲が乙に対して別途指示することとし、支払にともない発生する費用は、乙の負担とする。
&lt;/p&gt;
&lt;p&gt;
	2.乙は、出店料等の支払いを期限までにしない場合、甲に対し、当該期限日の翌日から完済日まで年利14.5%の遅延損害金を支払うものとする。
&lt;/p&gt;
&lt;p&gt;
	3.甲は、乙から受領した出店料等について、途中で本契約が終了した場合その他事由の如何を問わず返還しない。ただし、20条2項に基づき甲が乙の責に帰すべき事由がないにもかかわらず本契約を解約した場合、および20条5項の場合、にはこの限りではない。
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	第13条
&lt;/p&gt;
&lt;p&gt;
	守秘義務
&lt;/p&gt;
&lt;p&gt;
	1.甲および乙は、本契約期間中または本契約終了後にかかわらず、本契約および本契約に関連して知り得た情報、その他相手方の機密に属すべき一切の事項（以下、「秘密情報」という。）を第三者に漏洩・開示・提供してはならない。ただし、あらかじめ相手方の書面による承諾を得た場合には、この限りではない。
&lt;/p&gt;
&lt;p&gt;
	2.甲は、前項の定めにかかわらず、次の各号の一に該当する場合には、乙に関する秘密情報を開示することができる。ただし、甲は、開示することにより損害が生じると考えられる場合は事前に乙の承諾を得るものとする
&lt;/p&gt;
&lt;p&gt;
	（1）法令の定めにより官公庁その他機関から開示を求められた場合
&lt;/p&gt;
&lt;p&gt;
	（2）甲が、甲、顧客、乙以外の利用者その他第三者の権利、財産、の保護のために必要と判断した &amp;nbsp;場合
&lt;/p&gt;
&lt;p&gt;
	（3）甲が、Mr.Menuの運営のために必要と判断した場合
&lt;/p&gt;
&lt;p&gt;
	&lt;br /&gt;
&lt;/p&gt;
&lt;p&gt;
	&lt;strong&gt;第14条&lt;/strong&gt; 
&lt;/p&gt;
&lt;p&gt;
	禁止行為
&lt;/p&gt;
&lt;p&gt;
	乙は、以下の行為を行ってはならない。
&lt;/p&gt;
&lt;p&gt;
	1.法令又は本規約の定めに違反する行為またはそのおそれのある行為
&lt;/p&gt;
&lt;p&gt;
	2.公序良俗に反する行為
&lt;/p&gt;
&lt;p&gt;
	3.日本通信販売協会が定める広告に関する自主基準に違反する行為
&lt;/p&gt;
&lt;p&gt;
	4.消費者の判断に錯誤を与えるおそれのある行為
&lt;/p&gt;
&lt;p&gt;
	5.甲、他の出店者または第三者に対し、財産権（知的財産権を含む）の侵害、名誉・プライバシーの侵害、誹謗中傷、その他の不利益を与える行為、またはそのおそれのある行為
&lt;/p&gt;
&lt;p&gt;
	6.第三者のメールアドレス、パスワードまたはIDの不正使用
&lt;/p&gt;
&lt;p&gt;
	7.有害なコンピュータプログラム、メール等を送信する行為
&lt;/p&gt;
&lt;p&gt;
	8.Mr.Menuの提供に用いられているサーバーその他甲のコンピュータに過度な負荷をかけ、または不正にアクセスする行
&lt;/p&gt;
&lt;p&gt;
	9.甲、他の出店者または第三者の営業を妨害する行為
&lt;/p&gt;
&lt;p&gt;
	10.前各号の他、甲が不適当と判断する行為
